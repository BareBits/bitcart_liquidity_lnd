"""Default configuration for the Bitcart Liquidity Helper.

DO NOT EDIT THIS FILE for operational changes. There are three other
ways to override these defaults (highest precedence first):

  1. The Bitcart admin UI (when running as a plugin).
  2. Environment variables — set `LIQUIDITYHELPER_<NAME>=value` in the
     shell or systemd unit. The `LIQUIDITYHELPER_` prefix is required;
     bare `NAME=value` is ignored (with a stderr warning) so we can't
     accidentally pick up unrelated env vars set by other software
     (e.g. system tools owning generic names like STORE_NAME, LOG_LEVEL).
     Strings, ints, floats, bools (`true`/`false`), and `None` are
     auto-parsed. Works for standalone runs.
  3. A sibling `user_config.py` that imports anything it wants to
     override. See `user_config.py` for the minimal template.

The plugin reads the comment block above EACH setting in this file
and surfaces it as a tooltip in the admin UI. Format conventions:

  # === Section name ===
  #
  # Optional section-level paragraph (the parser ignores this — it's
  # for someone reading the file directly).

  # Description of the setting. May span multiple lines; the parser
  # joins them into one paragraph for the tooltip. Aim for 2-4 lines:
  # what it does, typical values, what breaks if it's misconfigured.
  SETTING_NAME: type = default_value

A blank line MUST separate the description block from earlier content;
the parser only treats the contiguous comment block immediately above
the assignment as that setting's description.
"""

from typing import List, Optional
import os
from os import environ

