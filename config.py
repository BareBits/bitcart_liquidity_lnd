# =============================================================================
# === Liquidity management mode ===
# =============================================================================
#
# Operator-facing top-level switch. The dashboard Settings tab renders a
# single dropdown ("LSP / Automatic / Disabled") that writes through to
# LIQUIDITY_DISABLED + AUTOMATIC_CHANNEL_CREATION_ENABLED (the latter lives
# in the LSP-funded block below). Both flags are hidden from the
# expansion-panel UI so the dropdown is the only authoritative entry
# point; they remain valid settings for env-var / user_config.py
# overrides.

# When True the entire tick loop is paused: run_tick_loop skips its
# call to main() each iteration and sleeps until either the operator
# flips this back to False or the plugin shuts down. Cashouts, fee
# payments, channel creation, topup notifications — everything in
# main() is gated by this. Dashboard endpoints (liquidity stats,
# settings, logs) continue to serve normally so the operator can
# diagnose and re-enable. Default True (engine paused) so a fresh
# install never spends operator funds without an explicit opt-in via
# the dashboard Settings dropdown.
LIQUIDITY_DISABLED: bool = True


# =============================================================================
# === LSP-funded inbound liquidity ===
# =============================================================================
#
# When AUTOMATIC_CHANNEL_CREATION_ENABLED is False (the default), the
# script delegates channel creation to an LSP via the LSPS1 protocol.
# Quotes are pulled from Zeus (lsps1.lnolymp.us) and Megalithic
# (megalithic.me); the cheaper one wins, with a slight Zeus preference
# on near-ties. The on-chain invoice for the LSP fee is paid from your
# wallet's on-chain balance.

# False (default) = delegate channel creation to an LSP via LSPS1.
# True = the script opens channels directly to peers it selects from a
# local candidate database, populated daily by querying your own LND
# node's gossip graph (legacy "pick your own peers" behavior — still
# fully automated, no operator interaction required). Most operators
# should leave this False; LSPs handle the operational pain (route
# reliability, channel-policy tuning) for a small one-time fee. When
# False, the daily gossip pull is skipped entirely — there's no need
# to maintain a candidate list when channel creation is delegated.
AUTOMATIC_CHANNEL_CREATION_ENABLED: bool = False

# Channel size requested from the LSP, in sats. This is your INBOUND
# liquidity per channel — the LSP funds their side, you pay the fee.
# Higher means fewer, larger channels (cheaper per sat of inbound).
# Cap at what you can actually use; idle inbound liquidity does
# nothing for you. Typical: 150_000 (150k).
LSP_CHANNEL_SIZE_SAT: int = 150_000

# Channel lease length in blocks. Channels naturally lapse at the end
# of the lease (the LSP can close them); liquidity_check then requests
# a fresh one. Zeus caps at 13_000 (~90 days) and Megalithic at
# 13_140 — 13_000 is the safe upper bound for both.
LSP_CHANNEL_EXPIRY_BLOCKS: int = 13_000

# Don't even ask an LSP for a quote when on-chain balance is below
# this — no realistic chance of paying any LSP fee. Typical: 1_000.
LSP_MIN_ONCHAIN_FOR_QUOTE_SAT: int = 1_000

# Throttle: at most ONE quote per LSP per wallet per this many hours.
# Each create_order registers a real (abandonable) order on the LSP's
# side; we don't want to spam them. Typical: 24.
LSP_QUOTE_THROTTLE_HOURS: int = 24

# Cap on the dynamic on-chain reserve floor. The effective reserve is
# max(MIN_RESERVE_ONCHAIN, recent LSP price peak), but never above
# this cap. Prevents a freak high quote from locking up the wallet
# indefinitely. Typical: 50_000.
LSP_RESERVE_CAP_SAT: int = 50_000

# Reject LSP quotes whose fee exceeds this fraction of the channel
# size. 0.01 = 1%. A 150_000-sat channel rejects any quote above
# 1500 sat. Raise to 0.05 (5%) if you want more leniency, or 1.0 to
# effectively disable. Also applied retroactively when computing the
# reserve floor. Typical: 0.01.
LSP_MAX_FEE_PERCENT: float = 0.01

# Auto-peer with each LSP's node URI at startup. LSPS1 requires LND
# to be peered before create_order is accepted. Throttled to once per
# day per wallet per LSP. Leave True unless you have a specific
# reason to manually manage peering.
LSP_AUTO_PEER: bool = True


# =============================================================================
# === Automatic-mode reserves ===
# =============================================================================
#
# Only consulted when AUTOMATIC_CHANNEL_CREATION_ENABLED=True. In LSP mode
# the on-chain reserve tracks recent LSP quotes (the LSP-funded inbound
# liquidity block above); in automatic mode the script pays directly for
# channel opens plus a loop-out to recover the local side as on-chain
# inbound. These constants estimate that cost.
#
# The automatic-mode reserve formula is:
#   target_liquidity
#     + MIN_CHANNEL_COUNT * AUTOMATIC_CHANNEL_OPEN_FEE_ESTIMATE_SAT * 2
#     + target_liquidity * AUTOMATIC_LIQUIDITY_LOOPOUT_FEE_PERCENT
#     + AUTOMATIC_RESERVE_SAFETY_SAT
# where target_liquidity = max(MIN_INBOUND_LIQUIDITY,
#                              MIN_INBOUND_LIQUIDITY_PER_CHANNEL * MIN_CHANNEL_COUNT).

# Per-channel on-chain fee estimate for opening (and later closing) a
# channel in automatic-creation mode. The formula multiplies this by
# (MIN_CHANNEL_COUNT * 2) to cover one open and one close per channel.
# Default 1000 sat — conservative for a low-priority funding tx.
# Raise it if you typically open at higher fee targets.
AUTOMATIC_CHANNEL_OPEN_FEE_ESTIMATE_SAT: int = 1_000

# Loop-out cost as a fraction of target inbound liquidity. The automatic-
# mode reserve assumes the script will fund inbound liquidity by opening
# channels and looping the local side back out to on-chain;
# this captures the swap-fee portion of that operation. 0.01 = 1%.
AUTOMATIC_LIQUIDITY_LOOPOUT_FEE_PERCENT: float = 0.01

# Safety buffer added on top of the automatic-mode reserve floor. Also
# acts as a hysteresis zone for low-on-chain warnings: if the wallet
# is within this many sats of the reserve floor, top-up warnings (log
# + SMTP) are suppressed. Action gates (refuse to open new channels,
# refuse to move on-chain to LN) still fire — the safety zone is only
# about not pestering the operator inside small fluctuations.
AUTOMATIC_RESERVE_SAFETY_SAT: int = 1_000


# =============================================================================
# === LND fee controls ===
# =============================================================================
#
# Per-tx miner-fee + per-payment routing-fee knobs for the LND-direct
# paths (SendCoins, SendPaymentSync, OpenChannelSync, CloseChannel).
# All paths previously inherited LND's hardcoded defaults or used
# placeholder values that were fine for regtest but wrong on mainnet.
#
# Defaults below are tuned for mainnet at typical congestion. The
# direct-channel cashout path uses a higher (cheaper) target_conf
# because channel opens are less urgent than payouts; cooperative
# closes use the standard 6-block target with a hard ceiling so a
# fee-estimator spike can't burn the channel's local balance.

# Block-confirmation target for on-chain SendCoins payments OTHER
# than cashouts (developer fee, referral fee, LSP order payment, etc).
# LND queries its fee estimator for the sat/vbyte rate that targets
# confirmation within N blocks. 6 = LND's own internal default
# (~30-60 min on mainnet); raise for cheaper but slower confirmation,
# lower for faster but more expensive. Cashouts use their own
# (slower, cheaper) target via LND_CASHOUT_TARGET_CONF below.
LND_ONCHAIN_TARGET_CONF: int = 6

# Block-confirmation target for on-chain cashout payments
# (CASHOUT_ONCHAIN sends labeled `lnhelper_cashout`). Set high
# because cashouts are not customer-facing urgent — the operator's
# revenue is already sitting in the wallet and the cashout just
# moves it to their preferred destination. A 144-block (~24-hour)
# target typically pays the very lowest sat/vbyte rate LND's fee
# estimator advertises, saving ~5-10x vs the 6-block default during
# normal mainnet conditions and ~20-50x during congestion. The
# trade-off is that the cashout may sit unconfirmed for up to a
# day, which is fine for routine wallet-drain operations.
LND_CASHOUT_TARGET_CONF: int = 144

# Explicit sat/vbyte override for on-chain SendCoins payments. When
# non-zero this wins over LND_ONCHAIN_TARGET_CONF. Use only when you
# need to force a specific feerate (e.g. operator manually picked a
# rate from mempool.space during a congestion spike). 0 = let
# LND_ONCHAIN_TARGET_CONF + LND's fee estimator decide.
LND_ONCHAIN_FEE_RATE_SAT_PER_VBYTE: int = 0

# Block-confirmation target for direct-channel cashouts
# (OpenChannelSync with push_sat). Channel opens are less urgent than
# regular payouts — a 12-block target halves the typical fee vs the
# 6-block default at the cost of one extra hour to confirmation,
# which doesn't matter because the LN funds become spendable only
# after 6 confs anyway.
LND_CHANNEL_OPEN_TARGET_CONF: int = 12

# Block-confirmation target for cooperative channel closes.
# Cooperative closes typically settle within hours; we want them
# confirmed promptly so the local balance becomes spendable, but
# they're not customer-facing urgent.
LND_CHANNEL_CLOSE_TARGET_CONF: int = 6

# Hard ceiling on the close tx's per-vbyte feerate. LND's fee
# estimator can spike during mempool congestion (multi-thousand
# sat/vbyte during 2024 inscription seasons); without a ceiling a
# close fee could eat a meaningful fraction of the channel's local
# balance. 50 sat/vbyte is plenty for "fast" under typical mainnet
# conditions and caps the worst case at ~25k sats per close on a
# 500 vbyte tx.
LND_CHANNEL_CLOSE_MAX_FEE_SAT_PER_VBYTE: int = 50

# Maximum CSV delay (in blocks) we'll allow on OUR local commitment
# output when opening a new channel. If WE force-close the channel,
# our `to_local` funds are CSV-locked by this many blocks before we
# can spend them. The peer can negotiate something HIGHER at channel
# open; we refuse and the open fails. Lower = faster recovery on
# force-close. Trade-off: lower CSV = less protection against a
# revoked-state attack (an attacker who steals our state during the
# CSV window could publish it). With anchor channels + a watchtower
# this risk is mitigated.
#
# 0 = don't override (let LND's daemon-level --max-local-csv-delay
# decide, default 2016 ~ 14 days). Setting this lower than 2016
# REQUIRES the remote peer to also configure a low CSV auto-pick;
# otherwise their auto-pick (typically 600 for typical channel sizes)
# will be > our cap and the OpenChannel fails. The plugin cannot
# coordinate this on its own — operators of OWN_LIGHTNING_NODES who
# want a sub-2016 cap must also set --max-local-csv-delay on the
# OWN node, and LSP channels inherit the LSP's CSV policy regardless.
# Default 0 to avoid breaking opens when the peer ships LND defaults.
#
# Only applies to channels OPENED BY US — peer-funded channels and
# LSP-funded inbound channels are negotiated by the other side and
# inherit their CSV policy. See move_onchain_to_ln and
# _attempt_direct_channel_cashout_to_own_node for the wire-through.
LND_MAX_LOCAL_CSV_BLOCKS: int = 0

# CSV delay (in blocks) WE impose on the REMOTE peer's `to_local`
# output. If THEY force-close, their funds are CSV-locked by this
# many blocks before they can spend them. Higher = more protection
# for us against the peer publishing a revoked state.
#
# 0 = let LND auto-scale by channel size (LND's default behavior;
# typically 144-2016 blocks depending on size). Operators usually
# leave this at 0 — lowering it weakens our cheating-protection
# without gaining us anything (the peer's funds being locked
# doesn't speed up our wallet recovery).
LND_REMOTE_CSV_DELAY_BLOCKS: int = 0

# Maximum LN routing fee (as a fraction of the payment amount) for
# every outgoing LN payment except the AMP fallback (which has its
# own cap baked in). Defends against a poorly-routed payment paying
# surprising fees — without this, LND's default fee_limit applies,
# which has shifted across LND versions.
# 0.02 = 2%, matches the engine's developer fee rate so a worst-case
# fee payment can't itself cost more than the fee being paid.
LN_PAYMENT_FEE_LIMIT_PERCENT: float = 0.02

# Absolute floor for the LN routing-fee cap. Used when a payment is
# so small that PCT × amount rounds to 0 (which LND's path-finder
# rejects). 50 sats covers a 5-hop route at 10 sat/hop.
LN_PAYMENT_FEE_LIMIT_MIN_SAT: int = 50


# =============================================================================
# === Channel rebalancing ===
# =============================================================================
#
# Periodic small circular rebalances that quietly cycle sats between
# channels to keep them in use. The point is to discourage peers from
# closing channels because they look idle / unbalanced — a tiny steady
# drip of traffic signals "this channel is active." Also helps
# maintain inbound capacity for customer payments by pushing sats out
# of over-full channels into emptier ones.
#
# Each tick (before fee payment and cashouts) the engine checks
# whether today's fee budget is still available and, if so, attempts
# a self-payment from the most over-full channel through the LN graph
# back into the most empty channel. The amount auto-adjusts via
# binary-halving until the routing fee fits within the budget.
#
# Sats *received* via a rebalance do NOT count as revenue — rebalance
# invoices are created via LND gRPC directly (not Bitcart's invoice
# store), so they never enter the revenue-walk. Fees paid for
# rebalances DO count as network fees and are credited toward the 2%
# developer-fee cap (same treatment as cashout / fee-payment fees).

# Yearly fee budget for automatic channel rebalancing. Daily allowance
# = total / 365. Unused daily allowance rolls over (the engine tracks
# the first-attempt date and computes available = daily × days_active
# − total_fees_spent). 0 disables the rebalancer entirely.
#
# Default 3000 sats/year (~8.2 sats/day) is intentionally small —
# the goal is "channel keep-alive" signaling, not active liquidity
# management. Operators wanting more aggressive rebalancing should
# raise this; tests show ~5-10× this for materially-frequent
# rebalances on typical mainnet fee conditions.
REBALANCE_YEARLY_BUDGET_SAT: int = 3000

# Per-channel safety reserve in sats. The rebalancer will never push
# a channel's local balance below this amount, and never attempt to
# move less than this amount (HTLCs below this are likely dust and
# won't route reliably). Doubles as the floor in the binary-halving
# amount search — when the halving probe drops below this floor with
# no viable fee, we move on to the next channel pair.
REBALANCE_MIN_CHANNEL_BUFFER_SAT: int = 1000


