# How long (in days) a peer remains audit-blacklisted after we close a
# channel with them for audit failure. During this period the peer is
# rejected by is_node_blacklisted as AUDIT_BLACKLISTED regardless of
# their current metrics. Gives the operator time to evaluate before
# re-engaging with a peer the script has decided to drop. Typical: 180.
CHANNEL_AUDIT_BLACKLIST_DAYS: int = 180

# Master switch for the coop-close retry loop. When True (default,
# opt-out), the script periodically re-issues cooperative close
# requests for channels that we asked to be closed but which haven't
# resolved (peer offline, never signed the closing tx). After
# CHANNEL_COOP_CLOSE_TIMEOUT_DAYS days of failed coop attempts the
# channel is escalated to force close. Set False to study stuck
# closes manually without the script intervening.
CHANNEL_COOP_CLOSE_RETRY_ENABLED: bool = True

# How often (in hours) the retry loop re-issues a coop close for a
# channel whose first close request hasn't resolved. LND doesn't
# auto-retry coop closes — if the peer was offline when we asked,
# the request is dropped on their end and we have to re-issue when
# they come back online. Hourly retries catch peers that pop up
# briefly without spamming LND. Typical: 1.
CHANNEL_COOP_CLOSE_RETRY_INTERVAL_HOURS: int = 1

# Days after the FIRST coop close request before escalating to force
# close. Force closes have a CSV timelock (typically days to weeks
# before our funds are spendable on-chain) which is why we wait this
# long before triggering — gives a flaky-but-occasionally-online peer
# plenty of chances to coop. Typical: 10.
CHANNEL_COOP_CLOSE_TIMEOUT_DAYS: int = 10

# Per-day cap on force closes initiated PER WALLET. Limits blast
# radius of any anomaly that flags many channels for escalation on
# the same day. Per-wallet (not global) so a healthy wallet isn't
# penalized by a sick one's force-close burst. Typical: 1.
CHANNEL_FORCE_CLOSE_MAX_PER_DAY_PER_WALLET: int = 1

# How long (in days) a peer remains blacklisted after we force-close a
# channel with them. Force close means coop close failed for the full
# CHANNEL_COOP_CLOSE_TIMEOUT_DAYS window — the peer was unreachable
# or refused to sign — so we don't re-engage for a long time. 365 days
# is significantly longer than the 180-day audit blacklist because
# force-close is a STRONGER negative signal: the peer wasn't just
# operating a low-quality node, they actively failed to coop with us.
# Independent from CHANNEL_AUDIT_BLACKLIST_DAYS — a peer could have
# both timestamps set, and is_node_blacklisted honors whichever
# expires later. Typical: 365.
CHANNEL_FORCE_CLOSE_BLACKLIST_DAYS: int = 365

# How often (minutes) the per-peer uptime tracker runs. Updates
# recent_uptime_checks / recent_failed_uptime_checks counters per peer
# of any open channel. Previously ran every tick (~90s) which made
# the threshold math nonsensical at scale; 10 minutes gives ~26,000
# samples per peer per 6-month window — enough for the failure-ratio
# gate below to be statistically meaningful. Operator-tunable; lower
# values mean more samples (finer-grained ratio) at the cost of more
# gRPC traffic. Typical: 10.
UPTIME_CHECK_INTERVAL_MINUTES: int = 10

# Length of the rolling window used to compute a peer's failure
# ratio. Counters (recent_uptime_checks, recent_failed_uptime_checks)
# reset to zero when current_window_started_at is older than this.
# Effect: a peer that was bad a year ago but has been perfect for the
# last 6 months earns automatic forgiveness — the old data is
# discarded. Typical: 180.
UPTIME_ROLLING_WINDOW_DAYS: int = 180

# Minimum number of days a peer must have been observed in the
# current window before the failure-ratio gate can fire. Protects
# against false-positives on peers that just got added or just had
# their window reset. At the default 10-min cadence, 90 days = ~13k
# samples, which makes a 2-day outage (~288 failures) ~2.2% of the
# window — well under the 5% threshold. So a single 1-2 day blip
# can't trigger blacklisting; only sustained unreliability does.
# Typical: 90.
UPTIME_MIN_OBSERVATION_DAYS: int = 90

# Fraction of failed uptime checks above which a peer is flagged as
# unreliable (HIGH_FAILURE_RATIO). 0.05 = 5%. Applied only after the
# UPTIME_MIN_OBSERVATION_DAYS warm-up has elapsed, so transient
# 1-2 day outages can't trip it. A peer chronically flaky at this
# rate over 90+ days is genuinely unreliable. Typical: 0.05.
UPTIME_MAX_FAILURE_RATIO: float = 0.05

# A peer whose last_seen_online is older than this many days is
# flagged as LONG_OUTAGE — they're not just having a bad day, they
# appear to have gone away. Replaces the legacy 48-hour
# OFFLINE_RECENTLY check, which would false-positive on routine
# maintenance windows. Typical: 14.
UPTIME_LONG_OUTAGE_DAYS: int = 14

# Minimum age (days) a candidate Lightning node must have been
# operational. Two years (730 days) is the operator-chosen "stable
# enough" threshold — beyond this, additional age stops correlating
# with reliability and we order purely by fee rate. Avoids fly-by-
# night nodes. Typical: 730.
NODE_CRITERIA_MINIMUM_AGE: int = 730

# Maximum outbound fee rate (parts-per-million) a candidate node may
# advertise. Computed as the median fee_rate_milli_msat across all the
# node's announced (and enabled) channel policies. 10_000 ppm = 1%.
# Nodes whose median outbound fee exceeds this are rejected — both
# because routing through them would be expensive AND because high
# fees usually correlate with low remaining outbound liquidity (nodes
# raise fees to discourage further depletion). Typical: 10_000.
NODE_CRITERIA_MAX_FEE_RATE_PPM: int = 10_000

# Multiplier applied to LSP_CHANNEL_SIZE_SAT to compute the minimum
# total announced capacity a node must have. Default 10 means a 150k
# channel size requires the candidate's total capacity to be ≥1.5M
# sat across all its channels. Acts as a proxy for "does this node
# have enough outbound liquidity to immediately drain a channel we
# open to it?". Channel balances are private — we can't query
# outbound directly — so this is the best pre-open heuristic.
# Typical: 10 (strict).
NODE_CRITERIA_OUTBOUND_CAPACITY_MULTIPLIER: int = 10


# Smallest liquidity request to send to an LSP. Below this the LSP's
# minimum-fee structure makes the request uneconomical. Legacy knob;
# the LSP-funded path uses LSP_CHANNEL_SIZE_SAT instead.
MIN_INBOUND_LIQUIDITY_REQUEST_AMOUNT: int = 50_000

# Don't move less than this from on-chain to LN when rebalancing.
# Legacy AUTOMATIC_CHANNEL_CREATION knob.
MIN_ONCHAIN_TO_LN_MOVEMENT: int = 20_000

# Initial channel size for the first channel opened on a fresh wallet.
# Used in the legacy automatic-channel-open path.
INITIAL_CHANNEL_SIZE: int = 20_000

# Target inbound liquidity to request per channel-open in the legacy
# automatic-channel-open path. LSP path uses LSP_CHANNEL_SIZE_SAT instead.
TARGET_INBOUND_LIQUIDITY: int = 500_000


# =============================================================================
# === Override resolution ===
# =============================================================================
#
# Precedence (highest wins): plugin UI > environment variables >
# user_config.py > config.py defaults above. Plugin UI overrides are
# applied via the settings_bridge module after import; env vars and
# user_config.py are resolved here at import time.

if os.path.exists("user_config.py"):
    from user_config import *  # noqa: F401,F403

# Prefix every env-var lookup with this so we can't accidentally pick
# up unrelated env vars set by other software in the operator's shell
# (e.g. `STORE_NAME`, `LOG_LEVEL`, `DEBUG_MODE` are GENERIC names that
# system tools or other apps might own). Matches the convention used
# everywhere else in the codebase for our explicit env vars
# (LIQUIDITYHELPER_DB_PATH, LIQUIDITYHELPER_NODE_DB_PATH,
# LIQUIDITYHELPER_LOG_DIR, etc.).
# Note: the loop also iterates over module-level imports brought in via
# `from typing import ...` and helper imports; a bare env var collision
# with one of those names will emit the same migration warning even
# though it's not a real setting.
ENV_PREFIX = "LIQUIDITYHELPER_"

# Env-var override loop. For each UPPER_SNAKE_CASE local defined above:
#   - if LIQUIDITYHELPER_<NAME> is set, replace the local default with
#     the parsed env-var value.
#   - else if the BARE <NAME> is set in the environment (possibly an
#     operator who hasn't updated to the prefixed form, possibly a
#     name collision with another app), print a one-shot stderr
#     warning so they notice the override didn't take effect.
# Supports basic type inference for the prefixed form:
# "None"/"True"/"False" (case-insensitive), integers, floats, otherwise
# string.
import sys as _sys

for entry in list(locals().items()):
    name = entry[0]
    value = entry[1]
    if name.startswith("_"):
        continue
    if name in {"environ", "ENV_PREFIX"}:
        continue
    prefixed = f"{ENV_PREFIX}{name}"
    if prefixed in environ:
        environ_var = environ.get(prefixed)
        if environ_var.upper() == "NONE":
            locals()[name] = None
            continue
        if environ_var.upper() == "TRUE":
            locals()[name] = True
            continue
        if environ_var.upper() == "FALSE":
            locals()[name] = False
            continue
        if environ_var != "":
            if is_integer(environ_var):
                locals()[name] = int(environ_var)
            elif is_float(environ_var):
                locals()[name] = float(environ_var)
            else:
                locals()[name] = environ_var
            continue
    elif name in environ:
        # Bare-name env var is set but no longer honored. Most likely
        # an operator who hasn't migrated to the prefixed form; could
        # also be a coincidental collision with another app — both
        # cases want the same fix (use the prefixed form, or unset).
        print(
            f"WARNING: env var {name!r} is set but ignored — use "
            f"{prefixed!r} to override the {name} config setting.",
            file=_sys.stderr,
        )
