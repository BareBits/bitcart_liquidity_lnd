# Unless otherwise stated, all settings apply per-wallet and BTC amounts
# are in sats.


# =============================================================================
# === Liquidity targets ===
# =============================================================================
#
# How much inbound Lightning liquidity the script should maintain. When
# any of these thresholds is missed, liquidity_check requests a new
# channel (via LSP if AUTOMATIC_CHANNEL_CREATION_ENABLED is False, else
# directly to a peer).

# Always maintain at least this many channels with inbound liquidity.
# Higher than ~5 wastes on-chain fees opening lots of small channels;
# 1 puts all your eggs in one channel and risks total inbound loss if
# that channel goes offline or is unilaterally force-closed. Typical: 2.
MIN_CHANNEL_COUNT: int = 2

# Minimum total inbound liquidity to keep across all channels, in sats.
# When total inbound falls below this, the script requests a new channel.
# Should be at least as large as your typical biggest expected invoice;
# higher values mean fewer channel-open events. Typical: 100_000 (100k).
MIN_INBOUND_LIQUIDITY: int = 100_000

# Require at least ONE channel with this much inbound, in sats. Prevents
# the "MIN_INBOUND_LIQUIDITY satisfied by a hundred tiny channels" failure
# mode — a single large incoming payment needs a big enough single channel
# to flow through. Typical: 50_000 (50k).
MIN_INBOUND_LIQUIDITY_PER_CHANNEL: int = 50_000

# Currently UNUSED — no code path reads MIN_RESERVE_TOTAL today. The
# active on-chain reserve floor is computed by
# `effective_min_reserve_onchain()` from MIN_RESERVE_ONCHAIN (LSP mode)
# or the AUTOMATIC_* constants (automatic mode); see those settings.
# Kept here as a placeholder for a future per-wallet total-reserve
# enforcement layer. Don't rely on it for current behavior.
MIN_RESERVE_TOTAL: int = 20_000

# Sats kept specifically on-chain, never sent to cashout, available for
# opening a new LSP channel if liquidity gets tight. Combined with the
# 6-month LSP-quote high-water mark to form the effective on-chain
# reserve floor. Set this to your wallet's worst-case LSP fee.
# Typical: 10_000.
MIN_RESERVE_ONCHAIN: int = 10_000

# Per-channel on-chain buffer kept aside so each channel can be
# force-closed if its peer disappears (force-closes have a CSV-delayed
# sweep transaction whose on-chain fee comes from your wallet).
# Multiplied by the channel count to size the reserve. Typical: 500.
CHANNEL_ONCHAIN_BUFFER: int = 500


# =============================================================================
# === Cashout settings ===
# =============================================================================
#
# Where collected revenue goes. Each tick the script attempts an LN
# cashout (if enabled) and an on-chain cashout (if there's any on-chain
# revenue). If LN cashouts have been failing for
# CASHOUT_SWITCH_TO_ONCHAIN_AFTER_X_DAYS days, the script logs a
# stranded-funds warning and (when LOOP_OUT_ENABLED is True) drains the
# channels to on-chain via submarine swap.

# Enable LN-based cashouts. When True, each tick tries to send funds
# above the channel reserve to CASHOUT_LIGHTNING_ADDRESS. Set False
# only if you've moved entirely to on-chain or are diagnosing LN issues.
ENABLE_CASHOUT_LN: bool = True

# Enable on-chain cashouts. When True, on-chain revenue above the
# reserve floor is swept each tick to CASHOUT_ONCHAIN. Off by default
# because most operators prefer to keep funds flowing through LN.
ENABLE_CASHOUT_ONCHAIN: bool = False

# Prefer on-chain over LN even when LN is healthy. Forces every cashout
# attempt to the on-chain rail and triggers a loop-out drain of any
# accumulating LN balance. Use when you've decided LN custody isn't for
# you and want everything pulled to on-chain.
#
# If PREFER_LN_CASHOUT is also True, PREFER_LN_CASHOUT wins and this
# setting has no effect — the two are opposites and can't both apply.
PREFER_CASHOUT_ONCHAIN: bool = False

# Force all cashouts onto the Lightning rail. When True, on-chain revenue
# that would normally be swept to CASHOUT_ONCHAIN is instead used to
# open a NEW Lightning channel to a node we don't already have a channel
# with (picked by the same gossip-derived ranking the rest of the script
# uses). The LN-balance leg continues to sweep to
# CASHOUT_LIGHTNING_ADDRESS unchanged.
#
# Trade-off: if the amount currently eligible for cashout is below the
# minimum Lightning channel size (MIN_CHANNEL_SIZE_IN_SATS), the cashout
# is delayed until enough accumulates to open a channel. The benefit
# is automatic inbound-liquidity acquisition from a diverse set of
# nodes — every cashout-sized batch of on-chain revenue becomes a new
# routing partner — instead of paying out to a single external address.
#
# Works in both automatic and LSP modes: regardless of
# AUTOMATIC_CHANNEL_CREATION_ENABLED, this path always uses automatic-mode
# node selection so we control which peer we open to.
#
# Wins over PREFER_CASHOUT_ONCHAIN if both are True.
PREFER_LN_CASHOUT: bool = False

# Lightning nodes you control, in `pubkey@host:port` URI format.
# `host` can be an IPv4 address, IPv6 address (wrap in `[]`), or
# a v3 onion hostname. Multiple entries can be added — they're tried
# in the order listed.
#
# Purpose: keep your cashout fees as low as possible by sending funds
# DIRECTLY to your own node instead of routing through the public LN
# graph to a third-party Lightning Address. **These nodes do NOT need
# to be online 24/7** — a phone wallet, an occasional laptop, a Zeus
# remote-control node, etc. all work. The script just retries on each
# tick until the peer is reachable.
#
# WARNING: any node listed here that is NOT online when a customer
# pays a Bitcart invoice with Lightning may cause that payment to
# fail. The public Lightning graph treats announced channels as
# routable; if customers' payments try to route through a channel
# whose far end is offline, those payments fail at the offline hop
# and your customer sees a "no route" error at checkout. This is why
# OWN_LIGHTNING_NODES_ANNOUNCE_CHANNELS (below) defaults to False —
# new channels opened to these peers are PRIVATE (unannounced) by
# default so the public graph never tries to route through them.
# If you DO announce these channels and your peer goes offline, you
# WILL lose customer payments. Only announce when you're sure the
# node is reliably reachable.
#
# What changes when a channel exists to a peer in this list:
#
#   - On-chain → LN cashouts (PREFER_LN_CASHOUT=True): instead of
#     opening a channel to a random peer + paying an invoice, the
#     script opens a channel directly to one of these nodes with
#     `push_sat` set to ~98% of channel capacity. Cashout amount lands
#     on YOUR node in one atomic on-chain operation, zero LN fees.
#
#   - LN → LN cashouts: existing local balance in channels to these
#     peers is keysent BACK to the peer (no invoice round-trip, no
#     LN routing fees beyond the direct hop). Treated as a normal LN
#     cashout for accounting / fees / dashboard. If your node rejects
#     the keysend (LND's --accept-keysend is OFF by default), the
#     engine automatically retries via AMP (spontaneous AMP payment,
#     accepted by LND with --accept-amp which IS on by default in
#     recent versions, plus CLN/LDK/most mobile wallets). For maximum
#     compatibility, enable BOTH on your destination node — the
#     engine prefers keysend (slightly cheaper protocol overhead) and
#     only falls back when keysend is rejected.
#
#   - Channel audit: these channels are SKIPPED. We assume your node
#     may be offline / on a phone / on tor — audit metrics computed
#     against an intermittently-reachable peer are noise.
#
#   - Automatic closure: NEVER. These channels are exempt from every
#     automatic close path (audit failure, force-close escalation,
#     etc.). Operator-initiated closes via lncli/UI still work.
#
#   - Inbound liquidity counting: these channels do NOT count toward
#     MIN_INBOUND_LIQUIDITY or MIN_CHANNEL_COUNT. The script assumes
#     they can't reliably route incoming customer payments (since the
#     peer may be offline). LSP channels are still opened as usual to
#     provide routable inbound capacity.
#
# Fallback chain (on-chain → LN cashout): tries each entry in order.
# If one is offline or refuses the channel, moves to the next. If ALL
# entries fail, falls back to the random-peer channel-open path
# (which then cashes out via the existing CASHOUT_LIGHTNING_ADDRESS).
#
# Example: ["02abcd...@1.2.3.4:9735", "03ffee...@xyz.onion:9735"]
OWN_LIGHTNING_NODES: List[str] = []

# Whether new channels opened to OWN_LIGHTNING_NODES peers should be
# announced to the public Lightning graph.
#
# Default False (private/unannounced) because the typical OWN node is
# a mobile wallet, laptop, or otherwise not-always-online machine.
# Announced channels invite the public graph to route customer
# payments through them; an announced channel whose far end is offline
# turns "customer pays your store" into "customer sees 'no route'".
#
# Set True ONLY if the listed nodes are reliably online (a 24/7 home
# server, a hosted node, etc.). When True, NEW channels opened by the
# direct-channel cashout path are public/announced and can participate
# in routing.
#
# Existing channels keep whatever announcement status they had at
# open time — this toggle only affects NEWLY opened channels.
OWN_LIGHTNING_NODES_ANNOUNCE_CHANNELS: bool = False

# Smallest LN cashout the script will send, in sats. Below this it
# skips the attempt (network fees would dominate). Strike's minimum is
# 100 sats. Raise if you want fewer, larger payouts. Typical: 150.
MIN_LN_CASHOUT_IN_SATS: int = 150

# Lightning Address the LN leg cashes out to. Format `name@host`. Can
# be a custodian (Strike, CoinOS) or a self-hosted address. Required
# for ENABLE_CASHOUT_LN. Intentionally unset by default so a fresh
# install can't accidentally sweep funds to a placeholder address —
# the operator must set this to an address they control before
# enabling LN cashouts.
CASHOUT_LIGHTNING_ADDRESS: Optional[str] = None

# On-chain Bitcoin address for cashouts and (if LOOP_OUT_ENABLED) for
# loop-out drain output. Bech32 / P2TR / P2WPKH all accepted. Required
# when ENABLE_CASHOUT_ONCHAIN=True OR PREFER_CASHOUT_ONCHAIN=True;
# otherwise optional.
CASHOUT_ONCHAIN: Optional[str] = None

# Smallest on-chain payment the script will broadcast, in sats. Gates
# the on-chain cashout sweep AND the on-chain dev-fee + referral-fee
# fallback paths. Below this the tx fees would dominate the payout —
# fees accumulate until the threshold is reached, then go out in one
# tx. Raise to batch revenue/fees into larger, cheaper-per-sat sweeps.
# Typical: 25_000.
MIN_ONCHAIN_CASHOUT: int = 25_000

# If LN cashouts have been failing for this many days, prefer on-chain
# for cashouts on the next tick and (if LOOP_OUT_ENABLED is True) drain
# channels via submarine swap. Set to None to disable the staleness
# fallback entirely. Typical: 30.
CASHOUT_SWITCH_TO_ONCHAIN_AFTER_X_DAYS: Optional[int] = 30


